def aqi_to_category(aqi):
    if aqi <= 50:  return "Good"
    if aqi <= 100: return "Moderate"
    if aqi <= 150: return "Unhealthy for Sensitive Groups"
    if aqi <= 200: return "Unhealthy"
    if aqi <= 300: return "Very Unhealthy"
    return "Hazardous"

def aqi_to_health(aqi):
    if aqi <= 25:  return "Best"
    if aqi <= 50:  return "Healthy"
    if aqi <= 100: return "Moderate"
    if aqi <= 150: return "Unhealthy"
    if aqi <= 300: return "Very Unhealthy"
    return "Hazardous"

def aqi_to_quality(aqi):
    """Human-friendly 7-level quality label: Best → Worst."""
    if aqi <= 25:  return "Best"
    if aqi <= 50:  return "Better"
    if aqi <= 100: return "Good"
    if aqi <= 150: return "Moderate"
    if aqi <= 200: return "Bad"
    if aqi <= 300: return "Worse"
    return "Worst"

# ─────────────────────────────────────────────
# 2. Train Model 1: Health Risk Classifier
# ─────────────────────────────────────────────
FEATURES = ["CO AQI Value", "Ozone AQI Value", "NO2 AQI Value",
            "PM2.5 AQI Value", "PM10 AQI Value", "SO2 AQI Value"]

X = df[FEATURES].values

# Health risk model
le_health = LabelEncoder()
y_health = le_health.fit_transform(df["Health Risk"].values)

X_train, X_test, y_train, y_test = train_test_split(
    X, y_health, test_size=0.2, random_state=42, stratify=y_health
)

print("\n🌲 Training Health Risk Classifier (Gradient Boosting)...")
health_model = GradientBoostingClassifier(
    n_estimators=250, max_depth=6, learning_rate=0.1, random_state=42
)
health_model.fit(X_train, y_train)
y_pred = health_model.predict(X_test)
health_acc = accuracy_score(y_test, y_pred)
print(f"✅ Health Risk Accuracy: {health_acc * 100:.2f}%")
print(classification_report(y_test, y_pred, target_names=le_health.classes_))

# ─────────────────────────────────────────────
# 3. Train Model 2: Dominant Pollutant Classifier
# ─────────────────────────────────────────────
le_pollutant = LabelEncoder()
y_pollutant = le_pollutant.fit_transform(df["Dominant Pollutant"].values)

X_train2, X_test2, y_train2, y_test2 = train_test_split(
    X, y_pollutant, test_size=0.2, random_state=42, stratify=y_pollutant
)

print("\n🧪 Training Dominant Pollutant Classifier (Random Forest)...")
pollutant_model = RandomForestClassifier(
    n_estimators=200, max_depth=None, random_state=42, n_jobs=-1
)
pollutant_model.fit(X_train2, y_train2)
y_pred2 = pollutant_model.predict(X_test2)
pollutant_acc = accuracy_score(y_test2, y_pred2)
print(f"✅ Dominant Pollutant Accuracy: {pollutant_acc * 100:.2f}%")
print(classification_report(y_test2, y_pred2, target_names=le_pollutant.classes_))

# ─────────────────────────────────────────────
