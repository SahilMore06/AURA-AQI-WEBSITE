"""
Train Enhanced Multi-Output ML Models
──────────────────────────────────────
Using the enhanced global pollution dataset with 60+ cities:
  1. Health Risk Level — Best / Healthy / Moderate / Unhealthy / Very Unhealthy / Hazardous
  2. Dominant Pollutant — which chemical is the primary cause
  3. AQI Category — EPA-standard classification

Also generates comprehensive city rankings with:
  • Per-pollutant breakdowns
  • AI-powered control recommendations
  • Category-wise city groupings
  • Pollutant hotspot analysis
"""
